package aula4;

public class NumtoPalavra {

    public static void main(String[] args) {

        int num;

        num = 3;

        if (num == 1) {
            System.out.println("UM");
        }

        if (num == 2) {
            System.out.println("DOIS");
        }

        if (num == 3) {
            System.out.println("TRES");
        }

    }
}
